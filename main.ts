function tanDegrees (num: number) {
    return Math.tan(num / Math.PI / 180)
}
function sinDegrees (num: number) {
    return Math.sin(num / Math.PI / 180)
}
function cosDegrees (num: number) {
    return Math.cos(num / Math.PI / 180)
}
function atan2Degrees (num: number) {
    return Math.atan2(num / Math.PI / 180, 0)
}
